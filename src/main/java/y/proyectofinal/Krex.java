    private void btt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_guardarActionPerformed
        try {
            // Obtén el nuevo ID válido para el vehículo desde la base de datos

            int id_kardex = obtenerNuevoIdVehiculoFromDatabase();
            PreparedStatement ps = con.prepareStatement("INSERT INTO `kardex` (id_kardex, marca, proveedor, modelo, almacenamiento, cantidad, "
                        + "valor_unitario, total_entrada, fecha, cantidad_existencia, valorunit_existencia, valortotal_existencia, detalles) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?,?, ?, ?)");

            ps.setInt(1, id_kardex);
            ps.setString(2, cbx_marca.getSelectedItem().toString());
            ps.setString(3, cbx_proveedor.getSelectedItem().toString());
            ps.setString(4, cbx_modelo.getSelectedItem().toString());
            ps.setString(5, cbx_almacenamiento.getSelectedItem().toString());
            ps.setString(6, txt_cantidad.getText());
            ps.setString(7, txt_valorunitario.getText());
            ps.setString(8, lbl_valortotalentrada.getText());
            ps.setString(9, lbl_fecha.getText());
            ps.setString(10, lbl_cantidadexistencia.getText());
            ps.setString(11, lbl_valorunitexistencia.getText());
            ps.setString(12, lbl_valortotalentrada.getText());
            ps.setString(13, txt_detalles.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos registrados");

        } catch (SQLException e) {
            System.out.println("Error: " + e);

        }
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM kardex");

            // Limpiar la tabla muesrta antes de agregar nuevos datos
            DefaultTableModel model = (DefaultTableModel) tb_kardex.getModel();
            model.setRowCount(0);

            // Recorrer el resultado y agregar los datos a la tabla muesrta
            while (rs.next()) {
                int id_kardex = rs.getInt("id_kardex");
                String fecha = rs.getString("fecha");
                String detalles = rs.getString("detalles");
                String cantidad = rs.getString("cantidad");
                String valor_unitario = rs.getString("valor_unitario");
                String total_entrada = rs.getString("total_entrada");
                String cantidad_existencia = rs.getString("cantidad_existencia");
                String valorunitexis = rs.getString("valorunit_existencia");
                String valortotalexistencia = rs.getString("valortotal_existencia");

                // Agregar los datos a la tabla muesrta
                model.addRow(new Object[]{id_kardex, fecha, detalles, cantidad, valor_unitario, total_entrada, cantidad_existencia, valorunitexis, valortotalexistencia});
            }

        } catch (SQLException e) {
            System.out.println("Error al obtener los datos de la tabla: " + e);
        }


    }//GEN-LAST:event_btt_guardarActionPerformed

