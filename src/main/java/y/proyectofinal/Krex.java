    private int obtenerNuevoIdVehiculoFromDatabase() {
        int nuevoId = 0;

        try {
            Statement statement = con.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT MAX(id_kardex) FROM kardex");

            if (resultSet.next()) {
                int maxId = resultSet.getInt(1);
                nuevoId = maxId + 1;
            }
        } catch (SQLException e) {
            System.out.println("Error al obtener el nuevo ID: " + e);
        }

        return nuevoId;

    }

    private void CargarComboCategorias() {
        Connection cn = Conexionn.conectar();
        String sql = "select * from catalogo_kardex";
        Statement st;

        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            cbx_marca.removeAllItems();
            cbx_marca.addItem("Seleccione MARCA");
            while (rs.next()) {
                cbx_marca.addItem(rs.getString("marca"));
            }
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error al cargar categorias");
        }

    }

