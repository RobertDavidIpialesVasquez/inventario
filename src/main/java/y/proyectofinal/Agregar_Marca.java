    private int obtenerNuevoIdMarcasFromDatabase() {
        int nuevoId = 0;

        try {
            Statement statement = con.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT MAX(id_marca) FROM marcas");

            if (resultSet.next()) {
                int maxId = resultSet.getInt(1);
                nuevoId = maxId + 1;
            }
        } catch (SQLException e) {
            System.out.println("Error al obtener el nuevo ID: " + e);
        }

        return nuevoId;

    }

    private void proveedor() {
        Connection cn = Conexionn.conectar();
        String sql = "select * from catalogo_kardex";
        Statement st;

        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            cbx_proveedor.removeAllItems();
            cbx_proveedor.addItem("Seleccione PROVEEDOR");
            while (rs.next()) {
                cbx_proveedor.addItem(rs.getString("proveedor"));
            }
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error al cargar categorias");
        }

    }
