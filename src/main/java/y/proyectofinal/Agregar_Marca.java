    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:
        try {
            // Obtén el nuevo ID válido para el vehículo desde la base de datos

            int idmarca = obtenerNuevoIdMarcasFromDatabase();
            PreparedStatement ps = con.prepareStatement("INSERT INTO `marcas` (idmarca, Mar_nombre,Proveedor, Comentarios, fecha ) VALUES (?, ?, ?, ?, ?)");

            ps.setInt(1, idmarca);
            ps.setString(2, txt_nombremarca.getText());
            ps.setString(3, cbx_proveedor.getSelectedItem().toString());
            ps.setString(4, txt_comentarios.getText());
            ps.setString(5, lbl_fecha.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos registrados");

        } catch (SQLException e) {
            System.out.println("Error: " + e);

        }
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM marcas");

            // Limpiar la tabla muesrta antes de agregar nuevos datos
            DefaultTableModel model = (DefaultTableModel) tb_marca.getModel();
            model.setRowCount(0);

            // Recorrer el resultado y agregar los datos a la tabla muesrta
            while (rs.next()) {
                int id_marca = rs.getInt("idmarca");
                String Mar_nombre = rs.getString("Mar_nombre");
                String Proveedor = rs.getString("Proveedor");
                String Comentarios = rs.getString("Comentarios");
                String fecha = rs.getString("fecha");

                // Agregar los datos a la tabla muesrta
                model.addRow(new Object[]{id_marca, Mar_nombre, Proveedor, Comentarios, fecha});
            }

        } catch (SQLException e) {
            System.out.println("Error al obtener los datos de la tabla: " + e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

